package parser;

public class ForecastImage {
    public String title;
    public String link;
    public String url;
    public String width;
    public String height;

    public ForecastImage(String title,String link,String url,String width,String height){
        this.title = title;
        this.link = link;
        this.url = url;
        this.width = width;
        this.height = height;
    }
}
